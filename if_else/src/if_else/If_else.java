
package if_else;

public class If_else {

    public static void main(String[] args) {
        int a, b;
        a = 100;
        b = 200;
        System.out.println("a = " + a);
        System.out.println("b = " + b);        
        if (a > b){
            System.out.println(a + " is greater.");
        } else {
            System.out.println(b + " is greater.");
        }
    }
    
}
